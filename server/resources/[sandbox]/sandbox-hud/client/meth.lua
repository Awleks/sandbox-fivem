RegisterNUICallback("Meth:Start", function(data, cb)
	exports['sandbox-sounds']:UISoundsPlayFrontEnd(-1, "SELECT", "HUD_FRONTEND_DEFAULT_SOUNDSET")
	TriggerEvent("Drugs:Client:Meth:ConfirmCook", data)
	exports['sandbox-hud']:MethClose()
	cb("ok")
end)

RegisterNUICallback("Meth:Cancel", function(data, cb)
	exports['sandbox-sounds']:UISoundsPlayFrontEnd(-1, "BACK", "HUD_FRONTEND_DEFAULT_SOUNDSET")
	exports['sandbox-hud']:MethClose()
	cb("ok")
end)
